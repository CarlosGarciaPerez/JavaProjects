    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String muestra = null;
        double varN = 0; //
        int varZ = 0;
        double varP = 0;
        double varQ = 0;
        double varErrorPorcen = 0;
        double varDE = 0;  //Desviacion Estandar
        double varErroConver = 0;

        if (jRadioButton1.isSelected() && jRadioButton3.isSelected()) {
            varN = Double.parseDouble(jTextField1.getText());
            varZ = Integer.parseInt(jTextField2.getText());
            varErrorPorcen = Double.parseDouble(jTextField5.getText());
            varDE = Double.parseDouble(jTextField6.getText());
          
            muestra = String.valueOf(formuMuestra.muestraPobFinitaConDevStandar(varN, varZ, varDE, varErrorPorcen));
            jLabel11.setText(muestra);

        }

        if (jRadioButton1.isSelected() && jRadioButton4.isSelected()) {

            varN = Double.parseDouble(jTextField1.getText());
            varZ = Integer.parseInt(jTextField2.getText());
            varP = Double.parseDouble(jTextField3.getText());
            varQ = Double.parseDouble(jTextField4.getText());
            varErrorPorcen = Double.parseDouble(jTextField5.getText());
         
            muestra = String.valueOf(formuMuestra.muestraPobFinitaSinDesEstandar(varN, varZ, varP, varQ, varErrorPorcen));
            jLabel11.setText(muestra);
        }

        if (jRadioButton2.isSelected() && jRadioButton3.isSelected()) {

            varZ = Integer.parseInt(jTextField2.getText());
            varDE = Double.parseDouble(jTextField6.getText());
            varErrorPorcen = Double.parseDouble(jTextField5.getText());
                                                  
            muestra = String.valueOf(formuMuestra.muestraPobInFinitaCnDesvStandar(varZ, varDE, varErrorPorcen ));
            jLabel11.setText(muestra);
        }

        if (jRadioButton2.isSelected() && jRadioButton4.isSelected()) {

            varZ = Integer.parseInt(jTextField2.getText());
            varP = Double.parseDouble(jTextField3.getText());
            varQ = Double.parseDouble(jTextField4.getText());
            varErrorPorcen = Double.parseDouble(jTextField5.getText());
           
            muestra = String.valueOf(formuMuestra.muestraPobInfinitaSinDevStandar(varZ, varP, varQ, varErrorPorcen));
            jLabel11.setText(muestra);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

